#' Check that a toc_style argument is valid (1, 2, or 3)
#' @noRd
.check_toc_style <- function(style) {
  style <- suppressWarnings(as.integer(style))
  if (is.na(style) || !style %in% 1L:3L) {
    cli::cli_abort(
      c(
        "{.arg toc_style} must be an integer between 1 and 3.",
        "i" = "Use {.fn BusinessReport::list_toc_styles} to see available styles."
      )
    )
  }
  style
}

#' Check that a hex colour is plausibly valid
#' @noRd
.check_color <- function(color) {
  if (!grepl("^#[0-9A-Fa-f]{6}$", color)) {
    cli::cli_abort(
      "{.arg primary_color} must be a six-digit hex colour, e.g. {.val #1a3a5c}."
    )
  }
  color
}

#' Check that a font ID is in the registry
#' @noRd
.check_font_id <- function(font) {
  registry <- list_fonts()
  if (!font %in% registry[["id"]]) {
    cli::cli_abort(
      c(
        "{.val {font}} is not a recognised font id.",
        "i" = "Use {.fn BusinessReport::list_fonts} to see available fonts."
      )
    )
  }
  registry[registry[["id"]] == font, "typst_family"][[1L]]
}
